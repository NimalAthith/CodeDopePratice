package c3JavaOperator;
/*
 * Length and breadth of a rectangle are 5 and 7 respectively. Write a program to calculate the area and perimeter of the rectangle.
 * 
 */
public class C3L1Q1 {
	public static void main (String[] args) {
		int length = 5;
		int breadth = 7;
		
		System.out.println("Area of rectangle = " + (length * breadth) + (", Perimeter = " + 2*(length + breadth)));
		
	}

}
